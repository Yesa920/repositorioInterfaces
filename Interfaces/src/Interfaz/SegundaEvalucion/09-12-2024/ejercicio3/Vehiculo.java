package ejercicio_3;

public class Vehiculo {
	private int velocidad;
	
	private int capacidad;
	
	public Vehiculo(){
	}
	
	public int getVelocidad() {
		return velocidad;
	}
	public void setVelocidad(int vel) {
		this.velocidad = vel;
	}
	public int getCapacidad() {
		return capacidad;
	}

	public void setCapacidad(int capa) {
		this.capacidad = capa;
	}
}
