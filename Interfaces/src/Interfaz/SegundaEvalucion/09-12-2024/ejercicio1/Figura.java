package Segunda_evaluacion.ejercicio1;

public class Figura {
	private int area;
	private int base;
	private int altura;

	public Figura() {

	}

	public int getArea() {
		return area;
	}

	public void setArea(int are) {
		this.area = are;
	}

	public int getBase() {
		return base;
	}

	public void setBase(int bas) {
		this.base = bas;
	}

	public int getAltura() {
		return altura;
	}

	public void setAltura(int alt) {
		this.altura = alt;
	}

}
